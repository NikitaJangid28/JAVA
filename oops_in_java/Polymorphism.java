public class Polymorphism {
    public static void main(String[] args) {
        int a=12,b=6;
        System.out.println("Addition : " +a+b);
        System.out.println("Addition : " +(a+b));

        String x="Hello", y="Java";
        System.out.println("Merge : " +x+y);
        
        int num=5;
        System.out.println("increment : " +(++num));
        System.out.println("increment : " +(num++));
        System.out.println("increment : " +num);
    }
}
